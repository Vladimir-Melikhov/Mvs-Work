  import { useRouter } from 'vue-router'
  import { useAuthStore } from '../stores/authStore'
  
  const router = useRouter()
  const auth = useAuthStore()
  
  const form = ref({
    title: '',
    description: '',
    price: '',
    ai_template: '',
    tags: []
  })
  
  const newTag = ref('')
  const loading = ref(false)
  const error = ref('')
  
  // –í–∞–ª–∏–¥–∞—Ü–∏—è —Ñ–æ—Ä–º—ã
  const isFormValid = computed(() => {
    return form.value.title.trim() !== '' &&
           form.value.description.trim() !== '' &&
           form.value.price && 
           parseFloat(form.value.price) > 0
  })
  
  // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —Ç–µ–≥–∞
  const addTag = () => {
    const tag = newTag.value.trim().toLowerCase()
    if (tag && !form.value.tags.includes(tag) && form.value.tags.length < 10) {
      form.value.tags.push(tag)
      newTag.value = ''
      error.value = ''
    }
  }
  
  // –£–¥–∞–ª–µ–Ω–∏–µ —Ç–µ–≥–∞
  const removeTag = (idx) => {
    form.value.tags.splice(idx, 1)
  }
  
  // –°–æ–∑–¥–∞–Ω–∏–µ —É—Å–ª—É–≥–∏
  const createService = async () => {
    if (!isFormValid.value) {
      error.value = "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∑–∞–ø–æ–ª–Ω–∏—Ç–µ –≤—Å–µ –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–æ–ª—è"
      return
    }
    
    loading.value = true
    error.value = ''
    
    try {
      const payload = {
        ...form.value,
        price: parseFloat(form.value.price),
        // –î–∞–Ω–Ω—ã–µ –≤–ª–∞–¥–µ–ª—å—Ü–∞ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤ –∫–∞—Ä—Ç–æ—á–∫–µ
